#!/usr/bin/env python3
"""
This module contains a type-annotated function that sums two floats.
"""

def add(a: float, b: float) -> float:
    """
    Takes two float numbers and returns their sum.

    Args:
    a: First float number.
    b: Second float number.

    Returns:
    Sum of a and b as a float.
    """
    return a + b
